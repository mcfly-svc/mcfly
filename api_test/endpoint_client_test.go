package api_test

import (
  "testing"
  "net/http"
  "github.com/mikec/marsupi-api/client"
)

type EndpointClient struct {
	Test 				*testing.T
	Endpoint 		client.EntityEndpoint
}

func (self *EndpointClient) Create(JSON string) (*client.ClientResponse, *http.Response) {
	cr, res, err := self.Endpoint.Create(JSON)
  if err != nil {
    self.Test.Error(err)
  }
  return cr, res
}

func (self *EndpointClient) GetAll() (*client.ClientResponse, *http.Response) {
  cr, res, err := self.Endpoint.GetAll()
  if err != nil {
    self.Test.Error(err)
  }
  return cr, res
}

func (self *EndpointClient) Get(ID int64) (*client.ClientResponse, *http.Response) {
	cr, res, err := self.Endpoint.Get(ID)
  if err != nil {
    self.Test.Error(err)
  }
  return cr, res
}

func (self *EndpointClient) Delete(ID int64) (*client.ClientResponse, *http.Response) {
	cr, res, err := self.Endpoint.Delete(ID)
	if err != nil {
		self.Test.Error(err)
	}
	return cr, res
}
